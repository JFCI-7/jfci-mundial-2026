  // ── State ──
  let current = DEFAULT_LANG;
  try {
    // 1. URL override: ?lang=en|es (para testing y links profundos).
    const urlLang = new URLSearchParams(location.search).get("lang");
    if (urlLang === "es" || urlLang === "en") {
      current = urlLang;
    } else {
      // 2. localStorage.
      const stored = localStorage.getItem(STORAGE_KEY);
      if (stored === "es" || stored === "en") current = stored;
      // 3. Browser language.
      else if (typeof navigator !== "undefined" && navigator.language && navigator.language.toLowerCase().startsWith("en")) {
        current = "en";
      }
    }
  } catch (_) {}

  function t(key, vars) {
    const dict = I18N[current] || I18N[DEFAULT_LANG];
    let s = dict[key];
    if (s == null) s = I18N[DEFAULT_LANG][key] != null ? I18N[DEFAULT_LANG][key] : key;
    if (vars) {
      Object.keys(vars).forEach(k => {
        s = s.replace(new RegExp("\\{" + k + "\\}", "g"), vars[k]);
      });
    }
    return s;
  }

  function set(lang, reload) {
    if (lang !== "es" && lang !== "en") return;
    current = lang;
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (_) {}
    document.documentElement.setAttribute("lang", lang);
    document.documentElement.setAttribute("data-lang", lang);
    if (reload) {
      // Recarga limpia para que app.js re-renderice todo con el nuevo idioma.
      const u = new URL(window.location.href);
      u.searchParams.set("_lang", String(Date.now()));
      window.location.replace(u.toString());
    }
  }

  // API pública
  window.I18N = { t, set, get lang() { return current; }, dict: I18N };
  document.documentElement.setAttribute("lang", current);
  document.documentElement.setAttribute("data-lang", current);
